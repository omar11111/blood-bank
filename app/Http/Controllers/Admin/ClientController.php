    public function status($id)
    {
        $client=Client::findOrFail($id);
        if($client->is_active == 1){
            
            $client->is_active=0;
            $client->save();
            return back();
           

        }elseif ($client->is_active == 0) {
            $client->is_active=1;
            $client->save();
            return back();
        } 
           
        

       
